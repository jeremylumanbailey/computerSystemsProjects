#include <stdio.h>
#include <math.h>

int main(void)
{
    double mu = 0;
    double sigma = 2;
    double pi = 3.14159;
    double fx;
    double i;

    for (i = -8; i <= 8; i += 0.1)
    {
        fx = 1/(sigma * sqrt(2 * pi)) * exp( -pow((i - mu), 2) / (2.0*sigma*sigma) );
        printf("%5.1f, %10.8f\n", i, fx);
    }
}

