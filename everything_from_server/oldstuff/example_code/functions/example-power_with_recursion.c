/*
    raise integer to integer power using recursion
*/

#include <stdio.h>

int power(int, int);

int main(void)
{
    int i, j;
    for(i = 2; i <= 10; i++)
    {
        for(j = 0; j <= 5; j++)
            printf("%2d^%d %8d\n", i, j, power(i, j) );

        printf("\n");
    }
}

int power(int b, int e)
{
    if(e == 0)
        return 1;
    else
        return (b * power(b, e-1) );    
}


